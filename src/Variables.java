public class Variables {
    public static void main(String[] args){
        //Strings
        String firstName = "Oluwaseun";
        String lastName = "Oluwafemi";
        String fullName = firstName + ' ' + lastName;
        System.out.println(fullName);

        //Integers
        int num1 = 25;
        int num2 = 30;
        int ame = num1 + num2;
        System.out.println("The sum is equals to  =" + ' ' + ame);
        

        //Float
        float you = 12.99f;
        float me = 12.01f;
        float youMe = you + me;
        System.out.println(youMe);

        //Double
        double You = 12.9999999d;
        double Me = 12.01111111d;
        double YouMe = You + Me;
        System.out.println(YouMe);

        /* The difference between Float and Double is that the precision of float is only six or seven decimal digits
         while the precision of Double is 15 decimal digits */




        //Character
        char firstLetter = 'D';
        char secondLetter = 'M';
        System.out.println(firstLetter + secondLetter);






    }

}
