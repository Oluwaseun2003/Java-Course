public class Strings {
    public static void main(String[] args){
        String name = "My name is Oluwaseun Oluwafemi";
        //String Length
        System.out.println("The length of sring name is:" + ' ' + name.length());

        //To change Cases
        System.out.println(name.toLowerCase());
        System.out.println(name.toUpperCase());

        //To find a character in a String
        System.out.println(name.indexOf("Oluwafemi"));

    }
}
